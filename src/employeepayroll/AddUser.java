    private void Fillcombo(){
        
        try {
            String sql ="select * from employee";
            pst=conn.prepareStatement(sql);
            rst=pst.executeQuery();
            
            while(rst.next()) {
                String name =rst.getString("username");
                txt_combo.addItem(name);
                
                
            }
            
        }catch (Exception e){
            
            JOptionPane.showMessageDialog(null, e);  
        } finally {
            
            try{
                rst.close();
                pst.close();
                
            }
            catch(Exception e){
                
            }
        }
    
    }


