    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add record?","Add Record",JOptionPane.YES_NO_OPTION);
        if(p==0){        

        try {

            String sql ="insert into employee (Emp_ID,username,password,division) values (?,?,?,?) ";

            pst=conn.prepareStatement(sql);
            pst.setString(1,txtEmployeeID.getText());
            pst.setString(2,txtUsername.getText());
            pst.setString(3,txtPassword.getText());
            pst.setString(4,txtDivision.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null,"Data is saved successfully");

        }
        catch (Exception e)

        {
            JOptionPane.showMessageDialog(null,e);
        }
        finally {

            try{
                rst.close();
                pst.close();

            }
            catch(Exception e){

            }
        }
        }
      Fillcombo();

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to update record?","Update Record",JOptionPane.YES_NO_OPTION);
        if(p==0){
         try{
            
            String value1 = txtEmployeeID.getText();
            String value2 = txtUsername.getText();
            String value3 = txtPassword.getText();
            String value4 = txtDivision.getText();
            
            
            String sql= "update employee set ID='"+value1+"', Username='"+value2+"', Password='"+value3+"',Division='"+value4+"' where ID='"+value1+"' ";
            
            pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Record Updated");
            
        }catch(Exception e){
       JOptionPane.showMessageDialog(null, e);
        }
        finally {
            
            try{
                rst.close();
                pst.close();
                
            }
            catch(Exception e){
                
            }
        }
        }
       Fillcombo();
   
    }//GEN-LAST:event_btnUpdateActionPerformed

